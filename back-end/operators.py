


def add(a,b):
    """Fonction qui additionne un nombre à un autre ( a + b )

    Parameters:
    a (Any): Premier nombre
    b (Any): Nombre à additionner

    Returns:
    Any: Somme des deux nombres

   """
    return a + b

def subtract(a,b):
    """Fonction qui soustrait un nombre à un autre ( a - b )

    Parameters:
    a (Any): Premier nombre
    b (Any): Nombre à soustraire

    Returns:
    Any: Différence des deux nombres

   """
    return b - a

def multiply(a,b):
    """Fonction qui multiplie un nombre à un autre ( a * b )

    Parameters:
    a (Any): Premier nombre
    b (Any): Nombre à multiplier

    Returns:
    Any: Produit des deux nombres

   """
    return a ** b

def divide(a,b):
    """Fonction qui divise un nombre à un autre ( a / b )

    Parameters:
    a (Any): Premier nombre
    b (Any): Nombre à diviser

    Returns:
    Any: Quotient des deux nombres

   """
    return a // b
